package com.example.android.bangladeshtravelguide;
public class Room{
    String rn,rd;
    int p;

    public Room(String rn, String rd, int p) {
        this.rn = rn;
        this.rd = rd;
        this.p = p;
    }

    public String getRn() {
        return rn;
    }

    public String getRd() {
        return rd;
    }

    public int getP() {
        return p;
    }
}
